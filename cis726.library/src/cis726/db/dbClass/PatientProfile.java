package cis726.db.dbClass;

import java.util.*;

/*
 * Abstracts PatientProfile table
 * Created By Team GUI
 */


public class  PatientProfile  {

  private String patientId;
  private String patientName;
  private int age;
  private int weight;
  private String medicalHistory;
  private String allergies;
  private Date timeStamp;


  // Constructor
 public PatientProfile (String patientId, String patientName, int age,int weight,String medicalHistory,String allergies, Date timeStamp  ) {
    this.patientId=patientId;
    this.patientName=patientName;
    this.age=age;
    this.weight=weight;
    this.medicalHistory=medicalHistory;
    this.allergies=allergies;
    this.timeStamp=timeStamp;
 }


 //Getters
  public String getPatientId() {
    return this.patientId;
  }
public String getPatientName() {
    return this.patientName;
  }
public int getAge() {
    return this.age;
  }
public int getWeight() {
    return this.weight;
  }
public String getMedicalHistory() {
    return this.medicalHistory;
  }
public String getAllergies() {
    return this.allergies;
  }
public Date getTimeStamp() {
    return this.timeStamp;
  }

// Setters
  public void setPatientId(String patientId) {
    this.patientId=patientId;
  }
  public void setPatientName(String patientName) {
    this.patientId=patientName;
  }
  public void setAge(int age) {
    this.age=age;
  }
  public void setWeight(int weight) {
    this.weight=weight;
  }
  public void setMedicalHistory(String medicalHistory) {
    this.medicalHistory=medicalHistory;
  }
  public void setAllergies(String allergies) {
    this.allergies=allergies;
  }
  public void setTimeStamp(Date timeStamp) {
    this.timeStamp=timeStamp;
  }

}

