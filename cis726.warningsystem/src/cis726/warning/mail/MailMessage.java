package cis726.warning.mail;

import java.util.Date;

/*
 * This class is used for the method that retrieve's e-mails out of an account. It will return a vector
 * of MailMessage objects.
 * 
 * Author: Kenton Born
 */
public class MailMessage {
	private String subject; //subject of the email
	private String content; //content of the email
	private Date date; //date the e-mail was sent
	private String from; //person who sent the email
	
	public MailMessage(String from, Date date, String subject, String content) {
		this.from = from;
		this.date = date;
		this.subject = subject;
		this.content = content;
	}
	
	public String getSubject() {
		return subject;
	}
	public String getContent() {
		return content;
	}
	public Date getDate() {
		return date;
	}
	public String getFrom() {
		return from;
	}
}
